#To demonstrate the input and output statement in python
#A python program that computes the AREA OF A CIRCLE in PHASE 2
#Since PIE is constant as 3.142, and RADIUS is given to be any value
#Using the FORMULA : Area of a circle = pie * (radius**2)

#Constant value...
pie = 3.142

#Inputing...
radius = int(input("enter the value for radius =",))

#Formular...
area = pie * (radius**2)

#Outputing...
print("area: =", area)
