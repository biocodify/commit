#To demonstrate the input and output statement in python
#A python program that computes the PERIMETER OF A CIRCLE
#Since PIE is constant as 3.142, and RADIUS is given to be any value
#Using the FORMULA : Perimeter of a Circle = (2 * pie * radius)

#Constant value...
pie = 3.142

#Inputing...
radius = int(input("enter the value for radius =" ))

#Formular...
area = (2 * pie * radius)
print("Perimeter of a cirle =", area)
