#To demonstrate the input and output statement in python
#A python program that computes the AREA OF A SECTOR
#Using the FORMULA : Area of a Sector = ((radius**2 * angle) / 2 )

#Inputing...
radius = int(input("enter the value for radius =" ))
angle = int(input("enter the value for angle =" ))

#Formular...
area = ((radius**2 * angle) / 2 )

#Outputing...
print("Area of Sector =", area)
