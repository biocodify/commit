#A python program that computes the AREA OF A TRAPEZIUM
#Using the FORMULA : Area of a trapezium = ((base 1 + base 2) / 2) * height

#Inputing...
base_1 = int(input("enter the value for base_1 =" ))
base_2 = int(input("enter the value for base_2 =" ))
height = int(input( "enter tghe value for height ="))

#Formular...
area = ((base_1 + base_2) / 2) * height

#Outputing...
print("Area of Trapezium =", area)
